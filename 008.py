#Crie um algoritmo que leia um salário e simule um reajuste positivo de 60%.

#leitura de dado
salario_atual = float(input('Digite seu salário: R$ '))

#processamento de dados
salario_ajustado = salario_atual * 1.6

#retorno do dado
print(f'\nSeu salário foi reajustado para: R$ {salario_ajustado}')